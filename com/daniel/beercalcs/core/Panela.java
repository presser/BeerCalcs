package com.daniel.beercalcs.core;

public class Panela {
	private int id;
	private String nome;
	private double altura;
	private double diametro;
	private double correcao;
	
	public int getId() {
		return id;
	}
	
	public void setId(int value) {
		id = value;
	}
	
	public String getNome() {
		return nome;
	}
	
	public void setNome(String value) {
		nome = value;
	}
	
	public double getAltura() {
		return altura;
	}
	
	public void setAltura(double value) {
		altura = value;
	}
	
	public double getDiametro() {
		return diametro;
	}
	
	public void setDiametro(double value) {
		diametro = value;
	}
	
	public double getCorrecao() {
		return correcao;
	}
	
	public void setCorrecao(double value) {
		correcao = value;
	}
	 

}
